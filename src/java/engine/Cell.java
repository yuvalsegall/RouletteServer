package engine;

import java.awt.Color;

/**
 *
 * @author yuvalsegall
 */
public class Cell {

    private int value;
    private Color color;

    public Cell(int value, Color color) {
        this.value = value;
        this.color = color;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
}
